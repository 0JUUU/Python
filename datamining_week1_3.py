import math

x = 4
print(math.sqrt(x))
print(math.pow(x,2))
print(math.exp(x))
print(math.log(x,2))
print(math.fabs(-4))
print(math.factorial(x))

z = 0.2
print(math.ceil(z))
print(math.floor(z))
print(math.trunc(z))

z = 3*math.pi
print(math.sin(z))
print(math.tanh(z))

x = math.nan
print(math.isnan(x))

x = math.inf
print(math.isinf(x))